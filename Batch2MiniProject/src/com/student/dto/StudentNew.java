package com.student.dto;

public class StudentNew {
	
	private String firstName;
	
	private String latsName;
	
	private int age;
	
	private ParentNew parent;
	
	private Address address;
	
	
	/**
	 * @return the firstName
	 */
	public String getFirstName() {
		return firstName;
	}
	/**
	 * @param firstName the firstName to set
	 */
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	/**
	 * @return the latsName
	 */
	public String getLatsName() {
		return latsName;
	}
	/**
	 * @param latsName the latsName to set
	 */
	public void setLatsName(String latsName) {
		this.latsName = latsName;
	}
	/**
	 * @return the age
	 */
	public int getAge() {
		return age;
	}
	/**
	 * @param age the age to set
	 */
	public void setAge(int age) {
		this.age = age;
	}
	/**
	 * @return the parent
	 */
	public ParentNew getParent() {
		return parent;
	}
	/**
	 * @param parent the parent to set
	 */
	public void setParent(ParentNew parent) {
		this.parent = parent;
	}
	/**
	 * @return the address
	 */
	public Address getAddress() {
		return address;
	}
	/**
	 * @param address the address to set
	 */
	public void setAddress(Address address) {
		this.address = address;
	}
	
	
	

}
