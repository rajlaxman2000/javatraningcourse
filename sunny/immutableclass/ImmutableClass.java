package immutableclass;

public final class ImmutableClass {
	
	final private String name;
	private final String email;
	
	public ImmutableClass(String name,String email){
		this.name=name;
		this.email=email;
		}
	public String getName(){
		return name;
	}
		public String getemail(){
			return email;
			}
		
	}
	
	
	
	
	


